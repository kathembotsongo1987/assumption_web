                <h1 style="border-radius: 5px; border-bottom: dashed; padding: 3px; text-align: center;">The impact of the College of the Assumption on the Congregation</h1>
                <p style="color:  white; font-style: solid; text-align: justify; margin: 2%;">
                  Augustinians of the Assumption, or ASSUMPTIONISTS.—This congregation had its origin in the College of the Assumption, established in Nimes, France, in 1843, by the Rev. Emmanuel d’Alzon vicar-general of that diocese, some account of whose life and work is given at the end of this article. Although it was organized in 1847, the members did not take their first vows until 1850; they took their public vows at Christmas of the next year. A second house was established in Paris, and they continued their work there, encouraged by the Holy See. The congregation was formally approved by a Brief of November 26, 1864. The chief objects of the congregation are to combat the spirit of irreligion in Europe and the spread of schism in the East. To this end the Assumptionists have devoted themselves to the work of Catholic higher and secondary education, to the spread of truth by means of the Press, to the conduct of pilgrimages, and to missionary work in the East. In addition to their college at Nimes they established Apostolic schools where poor students were educated for the priesthood without expense to themselves. They established “La Bonne Presse”, which issued periodicals, pamphlets, and books in great numbers, the chief publication, “La Croix”, appearing simultaneously in several different cities. Their activities provoked the resentment of the French Government, and in 1900 the congregation was suppressed within French territory, this action being based on the charge that they were accumulating a fund to be used in a royalist movement to overthrow the Republic. Many of the Assumptionists left France after this, but some remained as secular priests under the authority of various bishops. </p>
